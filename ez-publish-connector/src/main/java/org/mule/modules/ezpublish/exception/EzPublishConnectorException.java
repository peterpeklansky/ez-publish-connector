package org.mule.modules.ezpublish.exception;

public class EzPublishConnectorException extends Exception {

	private static final long serialVersionUID = 6095019701814853242L;

	public EzPublishConnectorException() {
		super();
	}

	public EzPublishConnectorException(String message, Throwable cause) {
		super(message, cause);
	}

	public EzPublishConnectorException(String message) {
		super(message);
	}

	public EzPublishConnectorException(Throwable cause) {
		super(cause);
	}

	
}
